class Person {
    constructor(name, age, Address) {
        this.name = name
        this.age = age
        this.Address = Address
    }
}

let person1 = new Person('Amal',20,'Ambalangoda')
console.log('Name :' + person1.name)
console.log('Age :' + person1.age)
console.log('Address :' + person1.Address)


